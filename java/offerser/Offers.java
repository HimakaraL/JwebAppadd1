package offerser;

public class Offers {
	
	private int oid;
	private String description;


	public Offers() {

	}
	
	public Offers(int oid, String description) {
	        this.oid = oid;
	        this.description = description;
	 }
	 
	public int getOid() {
		return oid;
	}
	
	
	public String getDescription() {
		return description;
	}
	

	


}